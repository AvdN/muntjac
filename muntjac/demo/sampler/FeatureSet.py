    _container = None
    _containerRecursive = False

    def __init__(self, *args):
        _0 = args
        _1 = len(args)
        if _1 == 2:
            pathname, content = _0
            self.__init__(pathname, pathname, '', content)
        elif _1 == 3:
            pathname, name, content = _0
            self.__init__(pathname, name, '', content)
        elif _1 == 4:
            pathname, name, desc, content = _0
            self._pathname = pathname
            self._name = name
            self._desc = desc
            self._content = content
            self.addFeature(self)
            if content is not None:
                for f in content:
                    if isinstance(f, FeatureSet):
                        continue
                    self.addFeature(f)
        else:
            raise ARGERROR(2, 4)

    @classmethod
    def addFeature(cls, f):
        if cls._pathnameToFeature is None:
            cls._pathnameToFeature = dict()
        if f.getFragmentName() in cls._pathnameToFeature:
            raise cls.IllegalArgumentException('Duplicate pathname for ' + f.getFragmentName() + ': ' + cls._pathnameToFeature[f.getFragmentName()].getClass() + ' / ' + f.getClass())
        cls._pathnameToFeature.put(f.getFragmentName(), f)

    def getFeature(self, pathname):
        return self._pathnameToFeature[pathname]

    def getFeatures(self):
        return self._content

    def getContainer(self, recurse):
        if (self._container is None) or (self._containerRecursive != recurse):
            self._container = HierarchicalContainer()
            self._container.addContainerProperty(self.PROPERTY_NAME, str, '')
            self._container.addContainerProperty(self.PROPERTY_DESCRIPTION, str, '')
            # fill
            self.addFeatures(self, self._container, recurse)
        return self._container

    def addFeatures(self, f, c, recurse):
        features = f.getFeatures()
        _0 = True
        i = 0
        while True:
            if _0 is True:
                _0 = False
            else:
                i += 1
            if not (i < len(features)):
                break
            item = c.addItem(features[i])
            property = item.getItemProperty(self.PROPERTY_NAME)
            property.setValue(features[i].getName())
            property = item.getItemProperty(self.PROPERTY_DESCRIPTION)
            property.setValue(features[i].getDescription())
            if recurse:
                c.setParent(features[i], f)
                if isinstance(features[i], FeatureSet):
                    self.addFeatures(features[i], c, recurse)
            if not isinstance(features[i], FeatureSet):
                c.setChildrenAllowed(features[i], False)

